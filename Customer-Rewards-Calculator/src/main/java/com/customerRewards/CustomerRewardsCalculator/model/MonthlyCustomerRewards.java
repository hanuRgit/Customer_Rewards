package com.customerRewards.CustomerRewardsCalculator.model;

public class MonthlyCustomerRewards {
	
	private int rewardMonth;
	private long monthlyRewards;
	
	public int getRewardMonth() {
		return rewardMonth;
	}
	public void setRewardMonth(int rewardMonth) {
		this.rewardMonth = rewardMonth;
	}
	public long getMonthlyRewards() {
		
		return monthlyRewards;
	}
	public void setMonthlyRewards(long monthlyRewards) {
		this.monthlyRewards = monthlyRewards;
	}

}
